#!/usr/bin/python3
def add(a, b):
    """Addition
    Args:
        a: first integer
        b: second integer
    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """Subtraction
    Args:
        a: first integer
        b: second integer
    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """Multiplication
    Args:
        a: first integer
        b: second integer
    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """Division
    Args:
        a: first integer
        b: second integer
    Returns:
        The return value. a / b
    """
    return int(a / b)
